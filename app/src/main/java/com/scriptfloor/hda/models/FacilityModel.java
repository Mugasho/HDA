public class FacilityModel {
    public String FacilityID;
    public String FacilityName;
    public String FacilityAddress;
    public String FacilitySector;
    public String FacilityCategory;
    public String FacilityLicense;
    public String Contact;
    public String Phone;
    public String Email;
    public String Qualification;
    public String FacilityLocation;
    public String CreatedAt;

    public String getFacilityID() {
        return FacilityID;
    }

    public void setFacilityID(String facilityID) {
        FacilityID = facilityID;
    }

    public String getFacilityName() {
        return FacilityName;
    }

    public void setFacilityName(String facilityName) {
        FacilityName = facilityName;
    }

    public String getFacilityAddress() {
        return FacilityAddress;
    }

    public void setFacilityAddress(String facilityAddress) {
        FacilityAddress = facilityAddress;
    }

    public String getFacilitySector() {
        return FacilitySector;
    }

    public void setFacilitySector(String facilitySector) {
        FacilitySector = facilitySector;
    }

    public String getFacilityCategory() {
        return FacilityCategory;
    }

    public void setFacilityCategory(String facilityCategory) {
        FacilityCategory = facilityCategory;
    }

    public String getFacilityLicense() {
        return FacilityLicense;
    }

    public void setFacilityLicense(String facilityLicense) {
        FacilityLicense = facilityLicense;
    }

    public String getContact() {
        return Contact;
    }

    public void setContact(String contact) {
        Contact = contact;
    }

    public String getPhone() {
        return Phone;
    }

    public void setPhone(String phone) {
        Phone = phone;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getQualification() {
        return Qualification;
    }

    public void setQualification(String qualification) {
        Qualification = qualification;
    }

    public String getCreatedAt() {
        return CreatedAt;
    }

    public void setCreatedAt(String createdAt) {
        CreatedAt = createdAt;
    }

    public String getFacilityLocation() {
        return FacilityLocation;
    }

    public void setFacilityLocation(String facilityLocation) {
        FacilityLocation = facilityLocation;
    }
}
